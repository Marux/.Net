using System;
namespace Solemne1
{
	public class Productos
	{
		public Productos()
		{
		}


        private int leche;
        private int pan;
        private int queso;

        public int getLeche()
        {
            return leche;
        }

        public void setLeche(int leche)
        {
            this.leche = leche;
        }

        public int getPan()
        {
            return pan;
        }

        public void setPan(int pan)
        {
            this.pan = pan;
        }

        public int getQueso()
        {
            return queso;
        }

        public void setQueso(int queso)
        {
            this.queso = queso;
        }


    }
}

